function [nBubbles, posBubbles, figSize] = makeBubbles_abs(xpos, ypos)

    nBubbles = [3, 4, 6, 6];

    % posBubbles = {[0 15], [0 20], [0 25], [5 30]} then use randi to add
    % subtract from those spaces
    posBubbles(1).b = {[xpos+0 ypos+15 16 16], ...
        [xpos+6 ypos+40 12 12], ...
        [xpos-10 ypos+65 8 8]};
    posBubbles(2).b = {[xpos+20 ypos+20 12 12], ...
        [xpos+16 ypos+50 12 12], ...
        [xpos-10 ypos+80 12 12], ...
        [xpos-30 ypos+105 8 8] };
    posBubbles(3).b = {[xpos+10 ypos+22 8 8], ...
        [xpos+0 ypos+40 8 8], ...
        [xpos+16 ypos+60 8 8], ...
        [xpos+0 ypos+70 8 8], ...
        [xpos+20 ypos+160 4 4], ...
        [xpos-20 ypos+170 4 4]};
    posBubbles(4).b = {[xpos+6 ypos+30 8 8], ...
        [xpos+0 ypos+80 6 6], ...
        [xpos+20 ypos+125 6 6], ...
        [xpos+16 ypos+165 6 6], ...
        [xpos+30 ypos+180 4 4], ...
        [xpos-50 ypos+183 4 4]};
    
    xS = [];
    xE = [];
    yS = [];
    yE = [];
    nAnimations = length(posBubbles);
    for iAnimations = 1 : nAnimations
        for iBubbles = 1 : nBubbles(iAnimations)
            xS = min([xS, posBubbles(iAnimations).b{iBubbles}(1) + posBubbles(iAnimations).b{iBubbles}(3), ...
                posBubbles(iAnimations).b{iBubbles}(1) - posBubbles(iAnimations).b{iBubbles}(3)]);
            xE = max([xE, posBubbles(iAnimations).b{iBubbles}(1) + posBubbles(iAnimations).b{iBubbles}(3), ...
                posBubbles(iAnimations).b{iBubbles}(1) - posBubbles(iAnimations).b{iBubbles}(3)]);
            yS = min([yS, posBubbles(iAnimations).b{iBubbles}(2) + posBubbles(iAnimations).b{iBubbles}(4), ...
                posBubbles(iAnimations).b{iBubbles}(2) - posBubbles(iAnimations).b{iBubbles}(4)]);
            yE = max([yE, posBubbles(iAnimations).b{iBubbles}(2) + posBubbles(iAnimations).b{iBubbles}(4), ...
                posBubbles(iAnimations).b{iBubbles}(2) - posBubbles(iAnimations).b{iBubbles}(4)]);
        end
    end
    
    
    xS = abs(xS); 
    xE = abs(xE);
    yS = abs(yS);
    yE = abs(yE);
    % redistribute bubbles according to absolute positions in figure
    for iAnimations = 1 : nAnimations
        for iBubbles = 1 : nBubbles(iAnimations)
           posBubbles(iAnimations).b{iBubbles}(1) = posBubbles(iAnimations).b{iBubbles}(1) + xS;
           posBubbles(iAnimations).b{iBubbles}(2) = posBubbles(iAnimations).b{iBubbles}(2) + yS;
        end
    end
    
    figSize.width = xS + xE;
    figSize.heigth = yE;
    
    
end